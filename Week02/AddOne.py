# AddOne.py
# This program reads a number and adds 1
# author: David Burke

# the users is prompted to enter a number
# the result is saved to a variable called num_in as a intiger
num_in = int(input("Enter your number here:"))

# 1 is added to the variable num_in
# the result is saved to variable num_out
num_out = num_in + 1

# the resulting number is printed to the command line
print("Next up is {}".format(num_out))